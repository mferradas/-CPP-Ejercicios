#include <iostream>
using namespace std;

int main()
{

    // si quisiera sumar decimales, debo utilizar 'double'

    // SUMA
    int a, b; // declaramos las variables
    cout << "Ingrese dos numeros (suma): " << endl; // pedimos los números
    cin >> a >> b; // ejecutamos el 'input'
    cout << a + b << endl; // mostramos la suma

    // RESTA
    int c, d; 
    cout << "Ingrese dos numeros (resta) : " << endl; 
    cin >> c >> d; 
    cout << c - d << endl;

    // MULTIPLICACIÓN
    int e, f;
    cout << "Ingrese dos numeros (multiplicacion): " << endl;
    cin >> e >> f;
    cout << e * f << endl;

    // DIVISIÓN
    int g, h;
    cout << "Ingrese dos numeros (division): " << endl;
    cin >> g >> h;
    cout << g / h << endl;






}

